package TileMap;

public class JsonObjectLayerObject {
	
	private String name;
	private String type;
	private int coordX;
	private int coordY;
	private int doorX;
	private int doorY;
	
public String getName() {return name;}
public void setName(String name) {this.name = name;}
public String getType() {return type;}
public void setType(String type) {}
public int getCoordX() {return coordX;}
public void setCoordX(int coordX) {this.coordX = coordX;}
public int getCoordY() {return coordY;}
public void setCoordY(int coordY) {this.coordY = coordY;}
public int getDoorX() {return doorX;}
public void setDoorX(int doorX) {this.doorX = doorX;}
public int getDoorY() {return doorY;}
public void setDoorY(int doorY) {this.doorY = doorY;}
}
